document.getElementById('search').addEventListener('input', function() {
    const query = this.value.toLowerCase();
    const results = document.getElementById('results');
    results.innerHTML = '';

    if (query) {
        for (const [key, value] of Object.entries(dictionary)) {
            if (key.toLowerCase().startsWith(query)) { // здесь изменено на startsWith
                const resultItem = document.createElement('div');
                resultItem.className = 'result-item';
                resultItem.textContent = `${key} - ${value}`;
                results.appendChild(resultItem);
            }
        }
        results.style.display = results.childNodes.length > 0 ? 'block' : 'none'; // Показываем или скрываем результаты
    } else {
        results.style.display = 'none'; // Скрываем, если нет запроса
    }
});

// Закрываем результаты при клике вне их
document.addEventListener('click', function(event) {
    const results = document.getElementById('results');
    if (!document.getElementById('search').contains(event.target)) {
        results.style.display = 'none';
    }
});
